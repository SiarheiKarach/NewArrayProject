package com.karach.arrayproject.exception;

public class ArrayModelException extends Exception {
  public ArrayModelException() {
    super();
  }

  public ArrayModelException(String message) {
    super(message);
  }

  public ArrayModelException(String message, Throwable cause) {
    super(message, cause);
  }

  public ArrayModelException(Throwable cause) {
    super(cause);
  }
}
